function join_art_side_by_side(art1, art2, separator)
    separator = separator or " " -- Default separator is a single space

    local lines1 = {}
    for line in art1:gmatch("([^\n]*)\n?") do
        table.insert(lines1, line)
    end

    local lines2 = {}
    for line in art2:gmatch("([^\n]*)\n?") do
        table.insert(lines2, line)
    end

    local max_lines = math.max(#lines1, #lines2)
    local result_lines = {}

    for i = #lines1 + 1, max_lines do table.insert(lines1, "") end
    for i = #lines2 + 1, max_lines do table.insert(lines2, "") end

    local max_width1 = 0
    for _, line in ipairs(lines1) do
        if #line > max_width1 then
            max_width1 = #line
        end
    end

    for i = 1, max_lines do
        local padded_line1 = lines1[i] .. string.rep(" ", max_width1 - #lines1[i])
        table.insert(result_lines, padded_line1 .. separator .. lines2[i])
    end

    return table.concat(result_lines, "\n")
end


